//Data type in Dart
// 1) Number => i) int ii) Double   2) String 3) Boolean

void main() {
  // int
  int score = 5;
  print(score);

  //Double
  double percentage = 95.4;
  print(percentage);

  //second Method

  var Score = 4;
  print(Score);

  var Percentage = 54.3;
  print(Percentage);

  //String

  String name = "Hassan";
  print(name);

  var Name = "Hassan Ali";
  print(Name);

  //Boolean

  bool Isvalid = true;
  print(Isvalid);

  var Invalid = false;
  print(Invalid);

// String
  String s1 = 'String ';
  String s2 = "String";
  String s3 = "It's Easy";
  String s4 = "Dart is programming language which is used for mobile";
  print(s1);
  print(s2);
  print(s3);
  print(s4);

  //String Interpolation

  String Lastname = 'Ali';
  print("Hassan $Lastname");
}
